import mongoose from 'mongoose';

const EnquirySchema = new mongoose.Schema({
    firstName: {
        type: String,
        required: true,
    },
    lastName: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    phoneNumber: {
        type: String,
        required: true,
    },
    projectType: {
        type: String,
        required: true,
    },
    message: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
});

const Enquiry = mongoose.models.Enquiry || mongoose.model('Enquiry', EnquirySchema);
export { Enquiry };
export default Enquiry;
